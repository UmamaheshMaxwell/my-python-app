# Python Operators

#  Operators are used to perform operations on variables and values.

#  Python divides the operators in the following groups:

#    1.Arithmetic operators
#    2.Assignment operators
#    3.Comparison operators
#    4.Logical operators
#    5.Identity operators
#    6.Membership operators
#    7.Bitwise operators


# Arithmetic operators - Arithmetic operators are used with numeric values
#                        to perform common mathematical operations

# + Operator

x = 1;
y = 2;

print(x + y)

# - Operator

a = 5
b = 3

print(a - b)

# * Operator

x = 3
y = 4

print(x * y)

# / Operator

a = 15
b = 3

# Below division Returns 5.0 using python3
# Follow, https://www.python.org/dev/peps/pep-0238/ for more info
print(a / b)

print(a // b)  # Returns 5 (The // operator will be available to request floor division unambiguously)

# % Operator

x = 7
y = 3

print(x % y)



